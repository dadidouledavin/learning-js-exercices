// Nous avons récupéré le nom des champions présent du coté bleu de la faille sous la forme d'une string
// Nous voulons avoir les champions avec une majusucle sur le première lettre
// Les fonctions .split, .map, .toUpperCase et .join pourrait être utile ici
// Pour vérifier vos résultats lancez la commande "npm run test:3" dans votre terminal

function test() {
  const equipe = 'shen nidalee ryze sivir alistar';

  // Le résultat doit être de la manière suivante : 'Shen Nidalee Ryze Sivir Alistar'
  // Envoyez votre résultat en faisant "return <variableContenantLeRésultat>"
  return equipe 
  .split(' ')
  .map(function (nom) {
    return nom.charAt(0).toUpperCase() + nom.slice(1);
  })
  .join(' ');
}

module.exports = test;
